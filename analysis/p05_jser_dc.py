"""
p05_jser_dc.py
====================
Replaces scalar-ASR JSER formula with per-sample computation, and adds
decision-change rate metrics (P0-A and P0-B corrections).

For each seed x lambda_r configuration, computes:

  ordinary_asr      : fraction of triggered samples clf predicts as target
  dc_rate           : fraction where clean source was NOT already target
  tpr_all           : detection rate over all triggered samples (old formula)
  jser_approx       : asr_json x (1 - tpr_all)            <- old (P05 formula)
  jser_persample    : mean(u_a & evades)                   <- P0-A corrected
  jser_dc           : mean(u_dc & evades)                  <- P0-B Prop-1 aligned
  dc_tpr            : TPR restricted to decision-changing successes
  already_target    : fraction of source samples already predicted as target
                      under clean inputs (outside Proposition-1 scope)

Uses identical R/C/E split as p05_indep_calibration_split.py.

Outputs (when external data are available):
  results/core_adaptive_comparison/indep_cal_mnist_jser_dc_metrics.csv
  results/core_adaptive_comparison/indep_cal_bloodmnist_jser_dc_metrics.csv

NOTE: this script requires external training data (model weights, raw dataset
samples) generated by training/scb_*_baseline.py and training/train_adaptive_qnn_fast.py.
Pre-computed results are available in the Zenodo deposit:
  https://doi.org/10.5281/zenodo.20700154

Theoretical context:
  jser_dc is the Tier-3 (empirical) realisation of Proposition 1's guarantee:
  it measures the fraction of genuine decision-change instances that both
  achieve attack success and evade detection -- precisely the event class for
  which D_Sigma > 0 is structurally guaranteed by {Z_k} subset S.
  The operational jser_persample additionally includes samples already
  predicted as the target class under clean inputs (already_target fraction).
"""

