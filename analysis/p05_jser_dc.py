# ── gate helpers ──────────────────────────────────────────────────────────────
_H_gate  = np.array([[1, 1],[1,-1]], dtype=np.complex128) / np.sqrt(2)
_HS_gate = np.array([[1,-1j],[1,1j]], dtype=np.complex128) / np.sqrt(2)

def _apply_1q(psi, gate, q):
    dim    = psi.shape[1]
    step   = dim >> (q + 1)
    groups = dim // (2 * step)
    return np.einsum("ij,bkjl->bkil", gate,
                     psi.reshape(-1, groups, 2, step),
                     optimize=True).reshape(-1, dim)

def get_xyz_feats(samples, weights, n_layers, n_qubits):
    """Returns [X|Y|Z] concatenated, shape (N, 3*n_qubits)."""
    try:
        z_ev, _, _ = _make_evals(n_qubits)
    except Exception:
        z_ev = np.zeros((n_qubits, 2**n_qubits))
        for q in range(n_qubits):
            for s in range(2**n_qubits):
                bit = (s >> (n_qubits - 1 - q)) & 1
                z_ev[q, s] = 1.0 - 2.0 * bit
    norms = np.linalg.norm(samples, axis=1, keepdims=True).clip(min=1e-12)
    psi   = (samples / norms).astype(np.complex128)
    wr    = weights.reshape(n_layers, n_qubits, 2)
    for l in range(n_layers):
        for q in range(n_qubits):
            ct, st = np.cos(wr[l, q, 0]/2), np.sin(wr[l, q, 0]/2)
            psi = _apply_1q(psi, np.array([[ct,-st],[st,ct]], dtype=np.complex128), q)
            ep  = np.exp(1j * wr[l, q, 1]/2)
            psi = _apply_1q(psi, np.array([[1/ep,0],[0,ep]], dtype=np.complex128), q)
        for q in range(n_qubits):
            dim    = psi.shape[1]
            n      = int(np.log2(dim))
            cb, tb = n-1-q, n-1-((q+1) % n_qubits)
            states = np.arange(dim, dtype=np.int64)
            psi    = psi[:, np.where((states >> cb) & 1, states ^ (1 << tb), states)]
    probs = np.abs(psi)**2
    Z = (probs @ z_ev.T).astype(np.float64)
    X = np.stack([(np.abs(_apply_1q(psi, _H_gate,  q))**2 @ z_ev[q])
                  for q in range(n_qubits)], axis=1)
    Y = np.stack([(np.abs(_apply_1q(psi, _HS_gate, q))**2 @ z_ev[q])
                  for q in range(n_qubits)], axis=1)
    return np.hstack([X, Y, Z]).astype(np.float64)

def get_z_feats(samples, weights, n_layers, n_qubits):
    """Returns only Z features, shape (N, n_qubits). Z is last n_qubits cols of XYZ."""
    xyz = get_xyz_feats(samples, weights, n_layers, n_qubits)
    return xyz[:, 2*n_qubits:]

# ── fixed two-layer MLP classifier (frozen head) ─────────────────────────────
class FixedClassifier:
    def __init__(self, w0, b0, w3, b3):
        self.w0, self.b0, self.w3, self.b3 = w0, b0, w3, b3

    def forward(self, z):
        h = np.maximum(0, z @ self.w0.T + self.b0)
        return h @ self.w3.T + self.b3

def load_clf(model_path: Path) -> FixedClassifier:
    state = torch.load(model_path, map_location="cpu", weights_only=False)
    return FixedClassifier(
        w0=state["classifier.0.weight"].numpy(),
        b0=state["classifier.0.bias"].numpy(),
        w3=state["classifier.3.weight"].numpy(),
        b3=state["classifier.3.bias"].numpy(),
    )

# ── Mahalanobis squared distance helper ──────────────────────────────────────
def mahal_sq(xyz, mu, prec):
    d = xyz - mu
    return np.einsum("ni,ij,nj->n", d, prec, d)

# ── core evaluation ───────────────────────────────────────────────────────────
def evaluate_v2(adap_weights, clf, x_clean_target, x_poison_triggered,
                x_source_clean, n_layers, n_qubits, target_class, asr_json):
    """
    adap_weights       : adaptive QNN weights (1-D numpy array)
    clf                : FixedClassifier loaded from baseline model
    x_clean_target     : [540, D] clean target-class samples for detector calibration
    x_poison_triggered : [60, D] triggered source samples
    x_source_clean     : [60, D] the same 60 source samples WITHOUT trigger
    n_layers, n_qubits : circuit parameters
    target_class       : integer (binary clf: class 1 = attack target)
    asr_json           : scalar ASR from JSON (for old-formula comparison)
    """
    assert len(x_clean_target) == R_SIZE + C_SIZE + E_SIZE

    # Independent calibration split (identical seed and sizes as p05)
    rng = np.random.default_rng(SPLIT_SEED)
    idx = rng.permutation(len(x_clean_target))
    x_r = x_clean_target[idx[:R_SIZE]]
    x_c = x_clean_target[idx[R_SIZE:R_SIZE+C_SIZE]]
    x_e = x_clean_target[idx[R_SIZE+C_SIZE:]]

    xyz_r = get_xyz_feats(x_r.astype(np.float64), adap_weights, n_layers, n_qubits)
    xyz_c = get_xyz_feats(x_c.astype(np.float64), adap_weights, n_layers, n_qubits)
    xyz_e = get_xyz_feats(x_e.astype(np.float64), adap_weights, n_layers, n_qubits)
    xyz_p = get_xyz_feats(x_poison_triggered.astype(np.float64), adap_weights, n_layers, n_qubits)

    lw   = LedoitWolf().fit(xyz_r)
    mu   = lw.location_
    prec = lw.get_precision()

    sc = mahal_sq(xyz_c, mu, prec)
    se = mahal_sq(xyz_e, mu, prec)
    sp = mahal_sq(xyz_p, mu, prec)

    tau     = float(np.percentile(sc, 95))
    evades  = sp <= tau                      # per-sample: NOT detected
    tpr_all = float(np.mean(sp > tau))       # TPR over all triggered samples
    fpr_e   = float(np.mean(se > tau))

    # AUC (threshold-free)
    y_all     = np.concatenate([np.zeros(len(xyz_e)), np.ones(len(xyz_p))])
    s_all     = np.concatenate([se, sp])
    auc_indep = float(roc_auc_score(y_all, s_all))
    xyz_all   = np.vstack([xyz_r, xyz_c, xyz_e])
    s_a2      = np.concatenate([mahal_sq(xyz_all, mu, prec), sp])
    y_a2      = np.concatenate([np.zeros(len(xyz_all)), np.ones(len(sp))])
    auc_all   = float(roc_auc_score(y_a2, s_a2))
    mahal_d   = float(np.sqrt(sp).mean())

    # P0-A: per-sample attack success (using adaptive weights + frozen classifier)
    z_p             = get_z_feats(x_poison_triggered.astype(np.float64), adap_weights, n_layers, n_qubits)
    preds_triggered = np.argmax(clf.forward(z_p), axis=1)
    u_a             = (preds_triggered == target_class)
    ordinary_asr    = float(np.mean(u_a))
    jser_persample  = float(np.mean(u_a & evades))          # P0-A corrected JSER
    jser_approx     = float(asr_json * (1 - tpr_all)) if asr_json is not None else None

    # P0-B: decision-change restricted metrics (Proposition-1 scope)
    z_src       = get_z_feats(x_source_clean.astype(np.float64), adap_weights, n_layers, n_qubits)
    preds_clean = np.argmax(clf.forward(z_src), axis=1)

    # u_dc: triggered -> target AND clean -> NOT target (genuine decision change)
    u_dc           = u_a & (preds_clean != target_class)
    dc_rate        = float(np.mean(u_dc))
    jser_dc        = float(np.mean(u_dc & evades))           # P0-B Prop-1 aligned JSER
    already_target = float(np.mean(preds_clean == target_class))
    dc_tpr         = float(np.mean(sp[u_dc] > tau)) if u_dc.sum() > 0 else float('nan')

    return dict(
        auc_indep      = round(auc_indep, 4),
        auc_all        = round(auc_all, 4),
        asr_json       = round(asr_json, 4) if asr_json is not None else None,
        ordinary_asr   = round(ordinary_asr, 4),
        dc_rate        = round(dc_rate, 4),
        already_target = round(already_target, 4),
        tpr_all        = round(tpr_all, 4),
        fpr_e          = round(fpr_e, 4),
        jser_approx    = round(jser_approx, 4) if jser_approx is not None else None,
        jser_persample = round(jser_persample, 4),
        jser_dc        = round(jser_dc, 4),
        dc_tpr         = round(dc_tpr, 4) if not np.isnan(dc_tpr) else None,
        mahal_d        = round(mahal_d, 4),
        tau            = round(tau, 4),
    )

