FIELDNAMES = [
    "seed", "lambda_r", "epoch",
    "auc_indep", "auc_all",
    "asr_json", "ordinary_asr", "dc_rate", "already_target",
    "tpr_all", "fpr_e",
    "jser_approx", "jser_persample", "jser_dc", "dc_tpr",
    "mahal_d", "tau",
]

def run_dataset(name, cfg):
    print(f"\n{'='*78}")
    print(f"Dataset: {name.upper()}  (n={cfg['n_qubits']}q, L={cfg['n_layers']})")
    print(f"{'='*78}")
    hdr  = f"{'seed':>5}  {'lam':>5}  {'asr_j':>7}  {'ord_asr':>8}  "
    hdr += f"{'dc_rate':>8}  {'alrdy_t':>8}  {'jser_ap':>8}  {'jser_ps':>8}  {'jser_dc':>8}"
    print(hdr)
    print("-" * 80)

    rows = []
    with open(cfg["out_csv"], "w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=FIELDNAMES)
        writer.writeheader()

        for lam in cfg["lambdas"]:
            for seed in cfg["seeds"]:
                pr_s       = f"{float(cfg['pr']):.12g}"
                wgt        = cfg["wgt_dir"] / f"weights_seed={seed}_L{cfg['n_layers']}_lr{lam}.npy"
                res        = cfg["res_dir"] / f"result_seed={seed}_L{cfg['n_layers']}_lr{lam}.json"
                base       = (cfg["data_dir"] / f"seed_{seed}"
                              / f"layer_{cfg['n_layers']}_grid"
                              / cfg["pair"] / f"pr_{pr_s}")
                meta_path  = base / f"poisoned_samples_meta_seed_{seed}_pr_{pr_s}.pt"
                model_path = base / "models" / f"clean_model_seed_{seed}_epoch_200.pt"
                if not model_path.exists():
                    model_path = base / "models" / "clean_model_seed_200.pt"

                missing = [p for p in [wgt, meta_path, model_path] if not p.exists()]
                if missing:
                    print(f"  SKIP (missing): {missing[0].name}")
                    continue

                adap_weights = np.load(wgt).astype(np.float64)
                meta         = torch.load(meta_path, map_location="cpu", weights_only=False)
                clf          = load_clf(model_path)

                x_ct        = meta["target_clean_data"].numpy().astype(np.float32)
                x_p         = meta["poisoned_non_target_data"].numpy().astype(np.float32)
                idx_src     = meta["poison_indices_in_non_target"].numpy()
                x_src_clean = meta["non_target_clean_data"][idx_src].numpy().astype(np.float32)

                if len(x_ct) != R_SIZE + C_SIZE + E_SIZE:
                    print(f"  WARNING: expected 540 clean, got {len(x_ct)} -- skip")
                    continue

                asr_json, ep = 1.0, 0
                if res.exists():
                    r        = json.load(open(res))
                    fin      = r.get("final", r.get("latest", {}))
                    asr_json = fin.get("asr", 1.0)
                    ep       = fin.get("epoch", 0)

                m   = evaluate_v2(adap_weights, clf, x_ct, x_p, x_src_clean,
                                  cfg["n_layers"], cfg["n_qubits"],
                                  cfg["target_class"], asr_json)
                row = dict(seed=seed, lambda_r=lam, epoch=ep, **m)
                writer.writerow(row)
                rows.append(row)
                print(f"  {seed:>5}  {lam:>5}  {m['asr_json']:>7.4f}  "
                      f"{m['ordinary_asr']:>8.4f}  {m['dc_rate']:>8.4f}  "
                      f"{m['already_target']:>8.4f}  {m['jser_approx']:>8.4f}  "
                      f"{m['jser_persample']:>8.4f}  {m['jser_dc']:>8.4f}")

    lam1 = [r for r in rows if r["lambda_r"] == 1.0]
    if lam1:
        print(f"\n--- lambda_r=1 summary (n={len(lam1)} seeds) ---")
        for key in ["jser_approx", "jser_persample", "jser_dc",
                    "dc_rate", "already_target", "ordinary_asr"]:
            vals = [r[key] for r in lam1 if r[key] is not None]
            if vals:
                print(f"  {key:20s}: mean={np.mean(vals):.4f}  "
                      f"pop_sd={np.std(vals):.4f}  "
                      f"vals={[round(v, 4) for v in vals]}")
    print(f"\nSaved -> {cfg['out_csv']}")
    return rows

